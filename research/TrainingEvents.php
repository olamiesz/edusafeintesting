	<h2>Work Package 2: Virtual and Augmented Reality technology
		development</h2>

	<p>The WP2 is focused on the development of a next generation of
		VR/AR systems for supporting personnel working in dangerous areas.</p>

	<p>The main features required are then reliability, flexibility,
		portability, and speed, features which are not found all together in
		the current state of the art systems.</p>

	<p>
		To reach this ambitious task the system will be developed <b>Pivoting</b>
		on a new hardware concept: the <b>WRM</b> (weighting resistive matrix)
		developed by <b>Roma2</b> (figure 1)
	</p>

	<p>
		<img src="research/WP2-fig1.png" width="100%" /><br />Figure 1: WRM
		building block
	</p>

	<p>This an analog computer based on a special resistive network
		able to perform at extreme speed a very good approximation of the
		least square fit between the data and a given hypothetical pattern,
		transforming the image in terms of probability distributions in the
		given pattern parametric space (Figure 2).</p>

	<p>
		<img src="research/WP2-fig2a.png" width="45%" /><img
			src="research/WP2-fig2b.png" width="55%" /><br />Figure 2: WRM
		transforms a test image in the circles parameter space
	</p>

	<p>The original concept of this invention comes from the need for
		high processing speed in the High Energy Physics experiments trigger
		systems, able to identify an elementary particle in a few nanoseconds.
	</p>

	<p>
		An efficient camera pose tracking system based on the exploitation of
		the <b>WRM</b> for pattern recognition is being developed jointly by <b>Roma2</b>
		and <b>EPFL</b> which has a very well established recognition in this
		field (Figure 3). Both parties will employ <b>2 ER</b> and <b>2
			ESR</b> (already hired) in key position for this development. The
		challenge is to submit the state of the art in the pattern recognition
		techniques to a deep revision to merge them with the features offered
		by the <b>WRM</b>.
	</p>

	<p>
		<img src="research/WP2-fig3a.png" width="32%"/><img src="research/WP2-fig3b.png" width="68%" /><br/>Figure
		3: Features matching algorithms through descriptors
	</p>

	<p>
		A crucial part of the system is the I/O and Wi-Fi communication
		infrastructure which will ensure the portability of the system. Strong
		requirements are about the necessity for a large and guaranteed
		bandwidth to permit real time A/V communication between operator and
		supervisor and to support a lag-less augmented reality experience. <b>NOCA</b>
		as responsible of this task will be supported by one <b>ESR</b>
		position.
	</p>

	<p>
		At the end of this chain the information stream will be used by the
		central system, managed by <b>CERN</b> for the generation of the
		augmented reality content in terms of animations, explanations and
		artificial highlighting of the relevant elements directly on the see
		through device. The process logic flow is illustrated in figure 4.
	</p>


